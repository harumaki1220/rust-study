fn main() {
    // 'let' を使って変数 'x' を宣言し、'5' という値を束縛（代入）する
    let x = 5;
    
    // '{}' プレースホルダーを使って、変数の値を表示する
    println!("xの値は: {}", x);

    // 変数はデフォルトで「イミュータブル（変更不可）」
    // x = 6; // この行のコメントを外すと、コンパイルエラーになる

    // 'mut' (ミュータブル) を付けると、変更可能な変数になる
    let mut y = 10;
    println!("yの最初の値は: {}", y);
    
    y = 20; // 'mut' が付いているので再代入OK
    println!("yの新しい値は: {}", y);
}